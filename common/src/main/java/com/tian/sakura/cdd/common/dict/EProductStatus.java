package com.tian.sakura.cdd.common.dict;

/**
 * 产品状态
 *  商品状态  0 未发布 1 已发布 未成交 2 已成交 3 已下架
 *
 * @author lvzonggang
 */
public enum  EProductStatus {

    OFF_SHELVES(3),
    PUBLISHED(1),
    TO_BE_PUBLISH(0);

    EProductStatus(int code) {
        this.code = code;
    }

    private int code;

    public int getCode() {
        return code;
    }
}
