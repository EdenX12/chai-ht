/**
 * 用户关系枚举 关系类型 0-预备队 1-近卫军
 *
 * @author lvzonggang
 */
public enum ERelationType {

    RESERVE_TEAM(0),
    GUARD(1);

    private int code;

    ERelationType(int code) {
        this.code = code;
    }

    public int getCode() {
        return code;
    }
}
