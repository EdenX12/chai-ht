package com.tian.sakura.cdd.common.dict;

/**
 * 0-未使用；1-已使用; 2-已失效
 *
 * @author lvzonggang
 */
public enum EUsedStatus {

    UNUSE("0"),
    USED("1"),
    INVALID("2");

    EUsedStatus(String code) {
        this.code = code;
    }

    private String code;

    public String getCode() {
        return code;
    }

}
